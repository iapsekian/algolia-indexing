function getThumbnail(input){
	if(input){
	    var protocolStr = '', urlStr = '';
	    var n = input.search('//');
	    if(-1 != n){
	        protocolStr = input.slice(0,n+2);
	        urlStr = input.slice(n+2);
	    } else {
	        urlStr = input.slice(0);
	    }
	    var tmp = urlStr.split('/');
	    var fileName = tmp[tmp.length-1];
	    urlStr = protocolStr;
	    for (var i = 0; i < tmp.length-1; i++) {
	        urlStr += tmp[i] + '/';
	    }
	    //handle filename for photo size
	    var fileTypeName = '';
	    var idxOfLastDot = fileName.lastIndexOf('.');
	    if(-1 !== idxOfLastDot){
	        fileTypeName = fileName.slice(idxOfLastDot+1);
	        fileName = fileName.slice(0,idxOfLastDot);
	    }
	    fileName += '_tb' + '.' + fileTypeName;

	    //urlStr += window.escape(fileName);
	    urlStr += fileName;
	    return urlStr;
	}
	return '';
};

